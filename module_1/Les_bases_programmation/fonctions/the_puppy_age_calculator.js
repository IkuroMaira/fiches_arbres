function calculateDogAge(agePuppy, ageHuman) {
    let resultDog = agePuppy * 7
    let resultHuman = ageHuman * 7
    console.log(`Your doggie is ${resultDog} years old in dog years! And you're ${resultHuman} years old in dog years.`)
}

calculateDogAge(7, 7)
calculateDogAge(9, 30)
calculateDogAge(17, 25)